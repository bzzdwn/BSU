% Заданная матрица
A = [1 2; 3 4];

% Найти обратную матрицу
invA = inv(A);

% Найти собственные значения и векторы
[eigenvecs, eigenvals] = eig(A);

% Найти определитель
detA = det(A);

% Вывести результаты
disp('Обратная матрица:');
disp(invA);

disp('Собственные значения:');
disp(diag(eigenvals));

disp('Собственные векторы:');
disp(eigenvecs);

disp('Определитель:');
disp(detA);

% Создать случайную матрицу 3x3
B = rand(3, 3);

% Заменить вторую строку единицами
B(2, :) = 1;

% Вывести результат
disp('Матрица после замены второй строки единицами:');
disp(B);
